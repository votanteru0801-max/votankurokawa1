def ganzhi_index(stem: str, branch: str) -> int:
    """天干・地支の組から60干支サイクル上のインデックス(0-59)を求める。"""
    for x in range(60):
        if STEMS[x % 10] == stem and BRANCHES[x % 12] == branch:
            return x
    raise ValueError(f"不正な干支の組み合わせです: {stem}{branch}")
